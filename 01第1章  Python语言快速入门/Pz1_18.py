def fun(_a, _b):
    _a, _b = _b, _a
    print("函数内交换数值后：a=%d,\tb=%d" % (_a, _b))


a = 10
b = 15
print("调用函数前的数值：a=%d,\tb=%d" % (a, b))
print("-----------------------------------")
fun(a, b)  # 调用函数
print("-----------------------------------")
print("调用函数后的数值：a=%d,\tb=%d" % (a, b))
